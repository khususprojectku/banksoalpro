// Database Engine Object
export const db = {
  // Initialize Database
  init() {
    if (!localStorage.getItem(DB_KEY)) {
      this.saveAll(DEFAULT_SEED_DATA);
      console.log('BankSoalPro Virtual DB successfully initialized with seed data.');
    }
  },

  // Save the entire database object to LocalStorage
  saveAll(data) {
    localStorage.setItem(DB_KEY, JSON.stringify(data));
  },

  // Retrieve the entire database object
  getAll() {
    this.init();
    try {
      return JSON.parse(localStorage.getItem(DB_KEY)) || DEFAULT_SEED_DATA;
    } catch (e) {
      console.error('Error parsing LocalStorage database, resetting...', e);
      this.saveAll(DEFAULT_SEED_DATA);
      return DEFAULT_SEED_DATA;
    }
  },

  // Get table records
  get(table) {
    const data = this.getAll();
    return data[table] || [];
  },

  // Save records to a specific table
  save(table, records) {
    const data = this.getAll();
    data[table] = records;
    this.saveAll(data);
  },

  // Insert a new record into a table
  insert(table, record) {
    const records = this.get(table);
    // Generate simple ID if not present
    if (!record.id) {
      const prefix = table.substring(0, 3);
      record.id = `${prefix}-${Date.now()}-${Math.floor(Math.random() * 1000)}`;
    }
    records.push(record);
    this.save(table, records);
    
    // Add to logs (except for logs table itself)
    if (table !== 'logs') {
      const activeUser = JSON.parse(sessionStorage.getItem('active_user') || 'null');
      if (activeUser) {
        this.log(activeUser.id, activeUser.name, `TAMBAH_${table.toUpperCase()}`, `Menambahkan data baru di tabel ${table} dengan ID ${record.id}`);
      }
    }
    return record;
  },

  // Update a record in a table by ID
  update(table, id, updatedFields) {
    const records = this.get(table);
    const index = records.findIndex(r => r.id === id);
    if (index !== -1) {
      records[index] = { ...records[index], ...updatedFields };
      this.save(table, records);

      // Add to logs
      if (table !== 'logs') {
        const activeUser = JSON.parse(sessionStorage.getItem('active_user') || 'null');
        if (activeUser) {
          this.log(activeUser.id, activeUser.name, `EDIT_${table.toUpperCase()}`, `Mengubah data di tabel ${table} dengan ID ${id}`);
        }
      }
      return records[index];
    }
    return null;
  },

  // Delete a record from a table by ID
  delete(table, id) {
    let records = this.get(table);
    const index = records.findIndex(r => r.id === id);
    if (index !== -1) {
      records.splice(index, 1);
      this.save(table, records);

      // Add to logs
      if (table !== 'logs') {
        const activeUser = JSON.parse(sessionStorage.getItem('active_user') || 'null');
        if (activeUser) {
          this.log(activeUser.id, activeUser.name, `HAPUS_${table.toUpperCase()}`, `Menghapus data di tabel ${table} dengan ID ${id}`);
        }
      }
      return true;
    }
    return false;
  },

  // Create an audit log record
  log(userId, userName, action, details) {
    const record = {
      id: `log-${Date.now()}-${Math.floor(Math.random() * 1000)}`,
      userId,
      userName,
      action,
      details,
      timestamp: new Date().toISOString()
    };
    const records = this.get('logs');
    records.unshift(record); // Prepend to show newest first
    // Limit to last 200 logs
    if (records.length > 200) {
      records.pop();
    }
    this.save('logs', records);
  },

  // Reset database to default seed data
  reset() {
    this.saveAll(DEFAULT_SEED_DATA);
    sessionStorage.removeItem('active_user');
    console.log('Database successfully reset to defaults.');
  }
};
